// NODE
class Node {
	constructor(val) {
		// - val
		this.val;
		// - next
		this.next = null;
		// - prev
		this.prev = null;
	}
}

// Doubly Linked List
class DoublyLinkedList {
	constructor() {
		// - head
		this.head = null;
		// - tail
		this.tail = null;
		// - length
		this.length = 0;
	}
}
